package com.mkyong;

public class Track {

	String title;
	String singer;

	public String getTitle() {
		return title;
	}

	public void setTitle(String title) {
		this.title = title;
	}

	public String getSinger() {
		return singer;
	}

	public void setSinger(String singer) {
		this.singer = singer;
	}


	public String toString() {
		return "Track [title=====" + title +",#$#rest2.2 singer=====" + singer + "]";
	}

}
